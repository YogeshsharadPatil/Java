public class StringInbuiltMathod {
    

    public static void main(String[] args) {
          String s1 = "PW Skills java";
          System.out.println(s1.toLowerCase());
          System.out.println(s1.toUpperCase());
          System.out.println(s1.length());
          System.out.println(s1.indexOf("a"));// it will print the first index of a
          System.out.println(s1.lastIndexOf("a")); // it will print the last index of the String 
          System.out.println(s1.charAt(0));
          System.out.println(s1.substring(0,5));// 5th index it will not take in a consideration
          

        
    }
}
